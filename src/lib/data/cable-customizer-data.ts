/**
 * Cable Builder Local Data
 * 
 * This file contains the Cable Builder data (cable series, cable types, connectors).
 * 
 * TO UPDATE THIS DATA:
 * 1. Provide the data manually or from your CMS export
 * 2. Update the arrays below with your actual data
 * 3. Ensure all prices are in DOLLARS (not cents)
 * 
 * Data Structure:
 * - cableSeries: Array of cable series (RG Series, ZDAL Series (LMR® Equivalent), etc.)
 * - cableTypes: Array of cable types with pricePerFoot
 * - connectors: Array of connectors with pricing per cable type
 */

export interface CableSeries {
  id: string;
  _id: string;
  name: string;
  slug: string;
  image?: string | null;
}

export interface CableType {
  id: string;
  _id: string;
  name: string;
  slug: string;
  series: string; // Series slug
  seriesName: string; // Series name
  pricePerFoot: number; // Price per foot in DOLLARS
  image: string | null;
  maxLength?: number | null;
  allowedConnectors?: string[] | null;
}

export interface ConnectorPricing {
  cableTypeSlug: string; // Cable type slug (lowercase, normalized)
  cableTypeName: string; // Cable type name
  price: number; // Price in DOLLARS
}

export interface Connector {
  id: string;
  _id: string;
  name: string;
  slug: string;
  image: string | null;
  pricing: ConnectorPricing[]; // Array of pricing per cable type
}

